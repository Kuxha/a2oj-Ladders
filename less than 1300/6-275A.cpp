#include<bits/stdc++.h>
using namespace std;
int main(){

	ios_base::sync_with_stdio(false); 
    cin.tie(NULL); 




    int a[3][3];
    int b[3][3];
    for(int i=0;i<3;i++){
    	for(int j=0;j<3;j++){
    		b[i][j] = 1;
    		cin>>a[i][j];
    		if(a[i][j]%2==0){
    			a[i][j] = 0;
    		}else{
    			a[i][j] = 1;
    		}
    	}
    }


    if(a[0][0]){
    	b[0][0] = !b[0][0];
    	b[1][0] = !b[1][0];
    	b[0][1] = !b[0][1];
    }


    if(a[1][0]){
    	b[0][0] = !b[0][0];
    	b[1][0] = !b[1][0];
    	b[2][0] = !b[2][0];
    	b[1][1] = !b[1][1];
    }

    if(a[2][0]){
    	b[2][0] = !b[2][0];
    	b[2][1] = !b[2][1];
    	b[1][0] = !b[1][0];
    }

    if(a[0][1]){
    	b[0][0] = !b[0][0];
    	b[0][1] = !b[0][1];
    	b[1][1] = !b[1][1];
    	b[0][2] = !b[0][2];
    }
    
    if(a[1][1]){
    	b[1][1] = !b[1][1];
    	b[0][1] = !b[0][1];
    	b[2][1] = !b[2][1];
    	b[1][0] = !b[1][0];
    	b[1][2] = !b[1][2];
    }
    
    if(a[2][1]){
    	b[1][1] = !b[1][1];
    	b[2][0] = !b[2][0];
    	b[2][1] = !b[2][1];
    	b[2][2] = !b[2][2];
    }

    if(a[0][2]){
    	b[0][1] = !b[0][1];
    	b[0][2] = !b[0][2];
    	b[1][2] = !b[1][2];
    }   

    if(a[1][2]){
    	b[0][2] = !b[0][2];
    	b[1][1] = !b[1][1];
    	b[1][2] = !b[1][2];
    	b[2][2] = !b[2][2];
    }  

 
    if(a[2][2]){
    	b[2][2] = !b[2][2];
    	b[2][1] = !b[2][1];
    	b[1][2] = !b[1][2];
    }  

	for(int i=0;i<3;i++){
    	for(int j=0;j<3;j++){
    		cout<<b[i][j];
    	}
    	cout<<"\n";
    }
	return 0;
} 

